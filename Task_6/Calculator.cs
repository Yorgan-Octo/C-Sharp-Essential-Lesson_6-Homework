using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Task_6
{
    public static class Calculator
    {

        public static double Add(double oper1, double oper2)
        {
            double sum = oper1 + oper2;
            return sum;

        }

        public static double Sub(double oper1, double oper2)
        {
            double sum = oper1 - oper2;
            return sum;

        }

        public static double Mul(double oper1, double oper2)
        {
            double sum = oper1 * oper2;
            return sum;

        }

        public static double Div(double oper1, double oper2)
        {
                double sum = oper1 / oper2;
                return sum;
        }


    }
}
